import math # need to import math module

num1 = 5

# Built-in number functions https://www.tutorialspoint.com/python/python_numbers.htm

print(math.exp(num1))

print(math.sqrt(num1))

print(math.sin(math.e)) # built-in e constant

print(math.sin(math.pi/2)) # half-pi is the equivalent of 90 degrees in radians (2-pi is 360 degrees)

